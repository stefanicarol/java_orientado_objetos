    public String armazenar(Carro cr){
        String saida="";
        carros.add(cr);
        for (Carro c : carros){
            saida += c.placa + " " + c.velCarro + "\n";
        }
        return saida;
    }
    
    public Carro existeCarro(String pl){
        for (Carro c : carros){
            if (c.placa.equalsIgnoreCase(pl)){
                return c;
            }
        }
        return null;
    }

