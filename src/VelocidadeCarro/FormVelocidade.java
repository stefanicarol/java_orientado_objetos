    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         cGlobal = new Carro();
        cGlobal.placa = tfPlaca.getText();
        cGlobal.velCarro = Double.parseDouble(tfVelocidade.getText());
        
        if (rbSim.isSelected()){
            taSaida.setText("Ambulância não paga multa!!!");
        }
        else
        {
            double valor = cGlobal.verificar();
            if (valor == 0){
                taSaida.setText("Não pagará multa!!!");
            }
            else
            {
                taSaida.setText("valor da multa = " + valor);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (cGlobal == null){
            taSaida.setText("Objeto não foi criado.\nEle é criado quando usa-se a opção verificar!!!");
        }
        else if (carros.contains(cGlobal)){ //verifica já tem o objeto (referência e não conteúdo)
            taSaida.setText("Objeto já inserido na lista.\n");
        }
        else if (existeCarro(cGlobal.placa) != null){
            taSaida.setText("Já existe com essa placa.\n");
        }
        else{
            taSaida.setText(armazenar(cGlobal));
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

