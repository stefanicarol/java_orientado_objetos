    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        taSaida.setText("Carros cadastrados:\n");
        for (Automovel carro : carros){
            taSaida.append("\tAntes de ligar - " + carro.marca
     		       + "\t" + carro.modelo
     		       + "\t" + carro.ano
                        + "\t" + carro.verEstado() + "\n");
            
            carro.ligar(); //liga o carro que acabou de imprimir
            
            taSaida.append("\tDepois de ligar - " + carro.marca
     		       + "\t" + carro.modelo
     		       + "\t" + carro.ano
                        + "\t" + carro.verEstado() + "\n\n");            
        }        // TODO add your handling code here:
       
    }//GEN-LAST:event_jButton1ActionPerformed

